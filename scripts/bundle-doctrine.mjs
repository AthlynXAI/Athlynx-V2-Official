// scripts/bundle-doctrine.mjs
// Build 27 — embeds /docs/specs/*.md as a TS module so the doctrine
// ships INSIDE the deploy bundle. Permanently baked, regardless of fs layout.
//
// Iron Sharpens Iron — Proverbs 27:17.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const specsDir = path.join(repoRoot, "docs", "specs");
const outFile = path.join(repoRoot, "server", "generated", "doctrine.ts");

fs.mkdirSync(path.dirname(outFile), { recursive: true });

const files = [
  "01-layer-cake-and-tokenization.md",
  "02-sport-classification-matrix.md",
  "03-autonomous-os.md",
  "04-quality-bar-4-5-star.md",
  "05-full-stack-layer-cake-v1.md",
];

const entries = files.map((f) => {
  const id = f.replace(/\.md$/, "");
  const md = fs.readFileSync(path.join(specsDir, f), "utf-8");
  return { id, markdown: md };
});

const out = `// AUTO-GENERATED by scripts/bundle-doctrine.mjs — do not edit by hand.
// Generated at ${new Date().toISOString()}.
// Source: docs/specs/*.md

export const DOCTRINE_GENERATED_AT = ${JSON.stringify(new Date().toISOString())};

export const DOCTRINE: Record<string, string> = ${JSON.stringify(
  Object.fromEntries(entries.map((e) => [e.id, e.markdown])),
  null,
  2,
)};
`;
fs.writeFileSync(outFile, out, "utf-8");
console.log(`[bundle-doctrine] wrote ${outFile} with ${entries.length} specs (${out.length} bytes)`);
